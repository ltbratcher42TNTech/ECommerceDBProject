USE ecommerce_db;

-- -------------------------------------------------------
-- 1. Total Revenue (uses only completed orders)
-- -------------------------------------------------------
SELECT 
	SUM(OrderTotal) AS total_revenue
FROM orders
WHERE OrderStatus = 'COMPLETED';

-- -------------------------------------------------------
-- 2. Revenue ordered by month
-- -------------------------------------------------------
SELECT 
    DATE_FORMAT(CreatedAt, '%Y-%m') AS month,
    SUM(OrderTotal) AS total_revenue
FROM orders
WHERE OrderStatus = 'COMPLETED'
GROUP BY month
ORDER BY month;

